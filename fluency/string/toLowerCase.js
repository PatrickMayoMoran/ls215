/*
* toLowerCase returns a new string with all the characters in lowercase
*/

let sentence = "Santa Cruz is the capital of Santa Cruz County in California.";
let scream = "WHY ARE WE SCREAMING?";

console.log(sentence); // "Santa Cruz is the capital of Santa Cruz County in California."
console.log(sentence.toLowerCase()); // "santa cruz is the capital of santa cruz county in california"
console.log(scream); // "WHY ARE WE SCREAMING?"
console.log(scream.toLowerCase()); // "why are we screaming?"
