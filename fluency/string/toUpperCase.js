/*
* toUpperCase converts the entire string to upper case
*/

let meow = "meow";
let numbers = "one1two2three3";
let other = "****meow****woof$$$$chaos!!!";

console.log(meow);
console.log(meow.toUpperCase());
console.log(numbers);
console.log(numbers.toUpperCase());
console.log(other);
console.log(other.toUpperCase());
